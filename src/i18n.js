export function getLang(){
  try{
    const saved = localStorage.getItem('mfj:lang');
    if(saved && T[saved]) return saved;
    const browser = navigator.language.split('-')[0];
    return T[browser] ? browser : 'it';
  }catch{ return 'it'; }
}
